/*
Definition:
A variable is a named container used to store data in memory.
It allows us to store values like numbers, strings, objects, or arrays
and use them later in the program.

Types:
1. let   - Value can be changed.
2. const - Value cannot be reassigned.
3. var   - Old way of declaring variables (Not used).
*/

// Example of a variable declaration using 'let'
let name = "Snehal";
console.log(name);

name = "Pragati";
console.log(name);

// Example of a variable declaration using 'const'

const gender = "Female";
console.log(gender);

// Example of a variable declaration using 'var' 
var age = 22;
console.log(age);

